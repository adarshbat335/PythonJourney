# Creating a list of my favorite fruits
favorite_fruits = ["Apple", "Banana", "Cherry"]

# Printing the whole list
print("My favorite fruits are:", favorite_fruits)

# Accessing a single item from the list (Python counts from 0)
first_fruit = favorite_fruits[0]  # This gets the first item
print("My absolute favorite is:", first_fruit)

# Adding a new fruit to the end of the list
favorite_fruits.append("Orange")

# Printing the list again to see the change
print("My updated list of fruits:", favorite_fruits)
